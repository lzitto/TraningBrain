public class livro {

    private String titulo;
    private String autor;
    private int ano;
    private String genero;
    private boolean lido;
    private int nota;
    
    public livro(String titulo, String autor, int ano, String genero, boolean lido, int nota) {
        this.titulo = titulo;
        this.autor = autor;
        this.ano = ano;
        this.genero = genero;
        this.lido = lido;
        this.nota = nota;
    }   
}
